                        sumBank.className = 'sum'
                        sumBank.innerHTML = sum
                        price.innerHTML = 'рублей'
                        sumBank.append(price)

                        stroke.append(logoBank, nameBank, categoryBank, bidBank, sumBank)
                        block.append(stroke)
                    }
