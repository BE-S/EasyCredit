                    function showBanks(result)
                    {
                        var bankBlock = document.querySelector('#bank-block')
                        var banks = document.querySelector('#banks')
                        banks.classList.toggle('active')
                        bankBlock.classList.toggle('active')
                        stayBlock.classList.toggle('active')

                        result.forEach(function (item, i) {
                                item.forEach(function (xui, j) {
                                    createBlock(banks, xui['name_bank'], xui['category'], xui['bid'], xui['sum'])
                                })
                        })
                    }

                    function createBlock(block, name, category, bid, sum)
                    {
                        let stroke = document.createElement('tr')
                        stroke.className = 'stroke'

                        let logoBank = document.createElement('td')
                        logoBank.className = 'logo-block'
                        let image = document.createElement('img')
                        image.className = 'logo'
                        image.src = '/public/images/Тест/Banks/' + name + '.svg'
                        logoBank.append(image)

                        let nameBank = document.createElement('td')
                        nameBank.className = 'name'
                        nameBank.innerHTML = name

                        let categoryBank = document.createElement('td')
                        categoryBank.className = 'category'
                        categoryBank.innerHTML = category

                        let bidBank = document.createElement('td')
                        bidBank.className = 'bid'
                        bidBank.innerHTML = bid

